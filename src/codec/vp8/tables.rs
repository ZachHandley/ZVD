#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
#[repr(u8)]
pub enum RefFrame {
    #[default]
    Intra = 0,
    Last = 1,
    Golden = 2,
    AltRef = 3,
}

/// Filter types
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum FilterType {
    #[default]
    Normal,
    Simple,
}

/// Color space
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum ColorSpace {
    #[default]
    YCbCr,
    Reserved,
}

/// Segment feature mode
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum SegmentFeatureMode {
    #[default]
    Delta,
    Absolute,
}

/// Intra prediction modes for 16x16 luma and 8x8 chroma
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
#[repr(u8)]
pub enum IntraMode16x16 {
    #[default]
    DcPred = 0,
    VPred = 1,
    HPred = 2,
    TmPred = 3,
}

impl TryFrom<u8> for IntraMode16x16 {
    type Error = ();

    fn try_from(value: u8) -> Result<Self, Self::Error> {
        match value {
            0 => Ok(IntraMode16x16::DcPred),
            1 => Ok(IntraMode16x16::VPred),
            2 => Ok(IntraMode16x16::HPred),
            3 => Ok(IntraMode16x16::TmPred),
            _ => Err(()),
        }
    }
}

